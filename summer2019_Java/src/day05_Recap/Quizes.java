		/*
        multiple ways to declare main method:
                1. public static void main(String[] args)
                2. static public void main(String[] args)
                3. public static void main(String...  args)
        
        */

	/*
	 \n (or\r): Starts new line
	 \t: Horizontal tab
	 \\: Prints single slash
	 \': Prints single quote
	 \": Prints double quote
	 
	 */
	
	System.out.println("today is great day\nFriday");
	System.out.println("\tTomorrow is Saturday, it's a day off");
	
	System.out.println("\\");
	System.out.println("\'Game of Thrones\'");    
		//This works as well 	System.out.println("'Game of Thrones'");
	System.out.println("\"Game of thrones\"");
	System.out.println("I like to read \"Books\"");
	
	
	
	
	
	
	
	
